export function AssetTable({ coins, category }) {

  const navigate = useNavigate();

  return (
    <Table className="relative">
      <ScrollArea className={category == "all" ? "h-[74vh]" : "h-[82vh]"}>
        <TableHeader>
          <TableRow className="sticky top-0 left-0 right-0 bg-card/95 backdrop-blur-md border-b border-emerald-500/20 hover:bg-card/95">
            <TableHead className="py-5 font-semibold text-emerald-400">Coin</TableHead>
            <TableHead className="font-semibold text-emerald-400">SYMBOL</TableHead>
            <TableHead className="font-semibold text-emerald-400">VOLUME</TableHead>
            <TableHead className="font-semibold text-emerald-400">MARKET CAP</TableHead>
            <TableHead className="font-semibold text-emerald-400">24H</TableHead>
            <TableHead className="text-right font-semibold text-emerald-400">PRICE</TableHead>
          </TableRow>
        </TableHeader>

        <TableBody>
          {coins.map((item, index) => (
            <TableRow
              className="cursor-pointer border-b border-emerald-500/5 hover:bg-emerald-500/5 transition-all duration-200 group"
              onClick={() => navigate(`/market/${item.id}`)}
              key={item.id}
            >
              <TableCell className="font-medium">
                <div className="flex items-center gap-3">
                  <Avatar className="h-10 w-10 ring-2 ring-emerald-500/20 group-hover:ring-emerald-500/40 transition-all duration-200">
                    <AvatarImage src={item.image} alt={item.symbol} />
                  </Avatar>
                  <span className="font-semibold group-hover:text-emerald-400 transition-colors">{item.name}</span>
                </div>
              </TableCell>
              <TableCell className="font-medium text-gray-400 uppercase">{item.symbol}</TableCell>
              <TableCell className="font-medium">
                ${(item.total_volume / 1000000).toFixed(2)}M
              </TableCell>
              <TableCell className="font-medium">
                ${(item.market_cap / 1000000000).toFixed(2)}B
              </TableCell>
              <TableCell
                className={`font-semibold ${item.market_cap_change_percentage_24h < 0
                    ? "status-negative"
                    : "status-positive"
                  }`}
              >
                {item.market_cap_change_percentage_24h > 0 ? "+" : ""}
                {item.market_cap_change_percentage_24h?.toFixed(2)}%
              </TableCell>
              <TableCell className="text-right font-bold text-lg">
                ${item.current_price?.toLocaleString()}
              </TableCell>
            </TableRow>
          ))}
        </TableBody>
      </ScrollArea>
    </Table>
  );
}

